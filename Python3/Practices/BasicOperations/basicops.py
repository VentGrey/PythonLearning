#!/usr/bin/env python

"""
Escriba un programa que dado como datos dos números reales, calcule la suma,
resta, multiplicación y división de dichos números.
"""

num1 = float(input("Ingrese el primer número a tratar: "))
num2 = float(input("Ingrese el segundo número a tratar: "))

print("La suma de los números es: ", num1 + num2)
print("La diferencia entre números es: ", num1 - num2)
print("El producto entre números es: ", num1 * num2)
print("La división de los números es: ", num1 / num2)
